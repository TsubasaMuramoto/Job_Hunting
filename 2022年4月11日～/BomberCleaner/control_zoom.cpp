#include "control_zoom.h"
#include "ui.h"

//=============================================================================
// マクロ定義
//=============================================================================
#define ZOOM_FRAME (40)

//=============================================================================
// コンストラクタ
//=============================================================================
CControlZoom::CControlZoom(void)
{
	m_nFrame = 0;
}

//=============================================================================
// デストラクタ
//=============================================================================
CControlZoom::~CControlZoom()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CControlZoom::Init()
{
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CControlZoom::Uninit()
{

}

//=============================================================================
// 更新処理
//=============================================================================
void CControlZoom::Update(CScene *pObject)
{
	m_nFrame++;

	CUi *pUi = nullptr;
	if (!pUi)
	{
		// オブジェクトをUIクラスにキャスト
		pUi = (CUi*)pObject;

		D3DXVECTOR3 pos;
		D3DXVECTOR3 size;

		pos = pUi->GetPos();
		size = pUi->GetSize();

		if (m_nFrame < ZOOM_FRAME)
		{
			size.x *= 1.15f;
			size.y *= 1.15f;

			pUi->SetPos(pos, size);
		}
	}


}

//=============================================================================
// 生成処理
//=============================================================================
CControlZoom *CControlZoom::Create()
{
	//インスタンスの生成
	CControlZoom *pControlZoom = nullptr;

	if (!pControlZoom)
	{
		pControlZoom = new CControlZoom;

		if (pControlZoom)
		{
			pControlZoom->Init();
		}
	}
	return pControlZoom;

}